
def luas():
    print("Luas persegi panjang")
    panjang = float(input("Masukkan panjang: "))
    lebar = float(input("Masukkan lebar: "))
    luas = panjang * lebar
    return luas

def keliling():
    print("Keliling persegi panjang")
    panjang = float(input("Masukkan panjang: "))
    lebar = float(input("Masukkan lebar: "))
    keliling = ( 2 * panjang ) + ( 2 * lebar)
    return keliling